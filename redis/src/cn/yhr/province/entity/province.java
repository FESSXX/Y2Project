package cn.yhr.province.entity;

public class province {
    private  int id;
    private String name;

    public int getId() {
        return id;
    }

    public province setId(int id) {
        this.id = id;
        return this;
    }

    public String getName() {
        return name;
    }

    public province setName(String name) {
        this.name = name;
        return this;
    }
}
